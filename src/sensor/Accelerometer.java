package sensor;

/**
 *
 * @author emmanuelsantana
 */
public class Accelerometer {

    private float X;
    private float Y;
    private float Z;

    public float getX() {
        return X;
    }

    public void setX(float X) {
        this.X = X;
    }

    public float getY() {
        return Y;
    }

    public void setY(float Y) {
        this.Y = Y;
    }

    public float getZ() {
        return Z;
    }

    public void setZ(float Z) {
        this.Z = Z;
    }
}
